Console.WriteLine("Hello World!"); // string
Console.WriteLine('b'); // char
Console.WriteLine(123); // int
Console.WriteLine(0.25F); // float, F, or f, is used after the number to tell the compiler this is a float.
Console.WriteLine(2.625); // double, decimals default to double when no literal suffix is entered.
Console.WriteLine(12.39816m); // decimal, M, or m, is used to specify a decimal literal.
Console.WriteLine(true); // bool
Console.WriteLine(false); // bool


string firstName = "Bob"; // Declaring and assigning a value to firstName variable
Console.WriteLine(firstName);
firstName = "Isabella"; // Reassigning a value
Console.WriteLine(firstName);